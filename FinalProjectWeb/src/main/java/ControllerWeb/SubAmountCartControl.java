package ControllerWeb;

import entity.Account;
import java.io.IOException;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

import Services.CartServices;
import ServicesImpl.CartServicesImpl;

@WebServlet(name = "SubAmountCartControl", urlPatterns = { "/subAmountCart" })
public class SubAmountCartControl extends HttpServlet {

	private static final long serialVersionUID = 1L;

	protected void processRequest(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		response.setContentType("text/html;charset=UTF-8");
		request.setCharacterEncoding("UTF-8");

		CartServices cartServices = new CartServicesImpl();

		HttpSession session = request.getSession();
		Account a = (Account) session.getAttribute("acc");
		if (a == null) {
			response.sendRedirect("login");
			return;
		}
		int accountID = a.getId();
		int productID = Integer.parseInt(request.getParameter("productID"));
		int amount = Integer.parseInt(request.getParameter("amount"));
		amount -= 1;

		cartServices.editAmountCart(accountID, productID, amount);
		request.setAttribute("mess", "Đã giảm số lượng!");
		request.getRequestDispatcher("managerCart").forward(request, response);
	}

	@Override
	protected void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		processRequest(request, response);
	}

	@Override
	protected void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		processRequest(request, response);
	}

	@Override
	public String getServletInfo() {
		return "Short description";
	}

}
